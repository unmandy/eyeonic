search.addEventListener('keyup', ()=>{
    const filter = document.getElementById('search').value.toUpperCase();
    let a = search_box.getElementsByTagName('a');
    for(let i = 0; i < a.length; i++){
        let b = a[i].getElementsByClassName('content')[0];
        let c = b.getElementsByTagName('h6');

        
        if (b.innerHTML.toUpperCase().indexOf(filter) > -1) {
            a[i].style.display = '';
            search_box.style.visibility = 'visible';
            search_box.style.opacity = 1;
        } else {
            a[i].style.display = 'none';
        }
        if (search.value == 0) {
            search_box.style.visibility = 'hidden';
            search_box.style.opacity = 0;
        }
    }
})









